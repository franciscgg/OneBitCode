#Antecessor e Sucessor de um número

number = int(input("Digite o número para descobrir seu antecessor e sucessor: "))
print(f"O antecessor de {number} é {number-1} e seu sucessor é {number+1}")

#Média de 4 notas
note1 = float(input("Digite a primeira nota: "))
note2 = float(input("Digite a segunda nota: "))
note3 = float(input("Digite a terceira nota: "))
note4 = float(input("Digite a quarta nota: "))

average = (note1 + note2 + note3 + note4) / 4

print(f"A média das notas é {average}")